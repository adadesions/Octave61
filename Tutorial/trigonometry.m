% Trigonometry
% Sin, Cos, Tan, asin, arc~

% Radian --> pi = 180
% sin(60), pi/3 = 60
disp('Radian')
fprintf('sin(pi/3) = %.3f\n', sin(pi/3));

% sin(30), pi/6 = 30
fprintf('sin(pi/6) = %.3f\n', sin(pi/6));
fprintf('cos(pi/6) = %.3f\n', cos(pi/6));
fprintf('tan(pi/6) = %.3f\n', tan(pi/6));

% tan(45), pi/4 = 45
fprintf('tan(pi/4) = %.3f\n', tan(pi/4));

% Degree
disp('Degree');
sind(30)
cosd(45)
tand(45)

% Arc
disp('Arc');
asin(0.5)
acos(1)












